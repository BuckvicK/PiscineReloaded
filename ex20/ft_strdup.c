#include <stdlib.h>

char	*ft_strdup(char *str)
{
	char	*res;
	int		i;

	i = 0;
	while (str[i] != '\0')
		i++;
	res = (char*)malloc(sizeof(char) * (i + 1));
	i = 0;
	while (str[i] != '\0')
	{
		res[i] = str[i];
		i++;
	}
	res[i] = '\0';
	return (res);
}
